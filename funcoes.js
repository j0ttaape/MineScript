export function danoZoombie(zumbi,steve){
    if(steve.armadura = 0){
        zumbi.dano = math.parseint(random()*5)
    }

    else if(steve.armadura = 1){

        zumbi.dano = math.parseint(random()*3.5)
    }

    else if(steve.armadura = 2){

        zumbi.dano = math.parseint(random()*2)
    }
    return zumbi.dano
}

